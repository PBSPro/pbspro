#include "portability.h"
#include "linked_list.h"
#ifndef NDEBUG
#include <stdio.h>
#include <stdlib.h>
#endif


/**
 * @file	linked_list.c
 * @brief
 * linked_list.c - general routines for maintenance of a double
 *	linked list.  A user defined structure can be managed as
 *	a double linked list if the first element in the user structure
 *	is the "pbs_list_node" struct defined in linked_list.h and the list
 *	is headed by a "pbs_list_node" struct also defined in linked_list.h.
 *
 * @par	These are the routines provided:
 *		insert_node - inserts a new entry before or after an old
 *		append_node - adds a new entry to the end of the list
 *		delete_node - removes an entry from the list
 *		is_in_list   - returns 1 if entry is in the list
 */


/**
 * @brief
 * 	-insert_node - adds a new entry to a list.
 *	Entry is added either before (position=0) or after (position !=0)
 *	an old entry.
 * 
 * @param[in] old	ptr to old entry in list 
 * @param[in] new  	ptr to new link entry
 * @param[in] pobj 	ptr to object to link in
 * @param[in] position  0=before old, else after
 *
 * @return	Void
 */

void
insert_node(struct pbs_list_node *old, struct pbs_list_node *new, 
 		void *pobj, int position)
{

#ifndef NDEBUG
	/* first make sure unlinked entries are pointing to themselves	    */

	if ((pobj          == (void *)0)	      ||
		(old           == (struct pbs_list_node *)0) ||
		(old->prev == (pbs_list_node *)0)        ||
		(old->next  == (pbs_list_node *)0)        ||
		(new->prev != (pbs_list_node *)new)      ||
		(new->next  != (pbs_list_node *)new)) {
		(void)fprintf(stderr, "Assertion failed, bad pointer in insert_node\n");
		abort();
	}
#endif

	if (position == NODE_INSET_AFTER) {  /* insert new after old */
		new->prev = old;
		new->next  = old->next;
		(old->next)->prev = new;
		old->next = new;
	} else {				/* insert new before old */
		new->next = old;
		new->prev = old->prev;
		(old->prev)->next = new;
		old->prev = new;
	}
	/*
	 * its big trouble if node data is null, it would make this
	 * entry appear to be the head, so we never let that happen
	 */
	if (pobj)
		new->data = pobj;
	else
		new->data = (void *)new;
}

/**
 * @brief
 * 	-append_node - append a new entry to the end of the list
 *
 * @param[in] head      ptr to head of list 
 * @param[in] new       ptr to new link entry
 * @param[in] pobj      ptr to object to link in
 *
 * @return      Void
 */

void
append_node(pbs_list_node *head, pbs_list_node *new, void *pobj)
{

#ifndef NDEBUG
	/* first make sure unlinked entries are pointing to themselves	    */

	if ((pobj == (void *)0) 		  ||
		(head->prev == (pbs_list_node *)0)   ||
		(head->next  == (pbs_list_node *)0)   ||
		(new->prev  != (pbs_list_node *)new) ||
		(new->next   != (pbs_list_node *)new)) {
		(void)fprintf(stderr, "Assertion failed, bad pointer in insert_node\n");
		abort();
	}
#endif

	(head->prev)->next = new;
	new->prev = head->prev;
	new->next  = head;
	head->prev = new;
	/*
	 * its big trouble if node data is null, it would make this
	 * entry appear to be the head, so we never let that happen
	 */
	if (pobj)
		new->data = pobj;
	else
		new->data = (void *)new;
}

/**
 * @brief
 * 	-delete_node - delete an entry from the list
 *
 * @par	Checks to be sure links exist before breaking them\n
 *	Note: the old entry is unchanged other than the list links
 *	are cleared.
 *
 * @param[in] old       ptr to link to delete 
 *
 * @return	Void
 *
 */

void
delete_node(struct pbs_list_node *old)
{

	if ((old->prev != (pbs_list_node *)0) &&
		(old->prev != old) && (old->prev->next == old))
		(old->prev)->next = old->next;

	if ((old->next != (pbs_list_node *)0) &&
		(old->next != old) && (old->next->prev == old))
		(old->next)->prev = old->prev;

	old->next  = old;
	old->prev = old;
}

/**
 * @brief
 * 	-swap_node - swap the positions of members of a list
 *
 * @param[in] pone - member one
 * @param[in] ptwo - member two
 * 
 * @return	Void
 * 
 */

void
swap_node(pbs_list_node *pone, pbs_list_node *ptwo)
{
	pbs_list_node *p1p;
	pbs_list_node *p2p;


	if (pone->next == ptwo) {
		delete_node(pone);
		insert_node(ptwo, pone, pone->data, NODE_INSET_AFTER);
	} else if (ptwo->next == pone) {
		delete_node(ptwo);
		insert_node(pone, ptwo, ptwo->data, NODE_INSET_AFTER);
	} else {
		p1p = pone->prev;
		p2p = ptwo->prev;
		delete_node(pone);
		insert_node(p2p, pone, pone->data, NODE_INSET_AFTER);
		delete_node(ptwo);
		insert_node(p1p, ptwo, ptwo->data, NODE_INSET_AFTER);
	}
}

/**
 * @brief
 * 	-is_in_list - determine if entry is in the list
 *
 * @param[in] head - ptr to head of list
 * @param[in] entry - entry to be searched for
 *
 * @return	int
 * @retval	1 	if in list
 * @retval	0 	if not in list
 *
 */

int
is_in_list(pbs_list_node *head, pbs_list_node *entry)
{
	pbs_list_node *pl;

	pl = head->next;
	while (pl != head) {
		if (pl == entry)
			return (1);
		pl = pl->next;
	}
	return (0);
}

/*
 * The following routines are replaced by in-line code with the
 * GET_NEXT / GET_PREV macroes when NDEBUG is defined, see linked_list.h
 */

#ifndef NDEBUG
/**
 * @brief
 *	get next entry in list
 *
 * @param[in] pl - list variable
 * @param[in] file -file name
 * @param[in] line - line num
 *
 * @retuan 	Void
 *
 */
void *
get_next(pbs_list_node pl, char *file, int line)
{
	if ((pl.next == (pbs_list_node *)0) ||
		((pl.next == &pl) && (pl.data != (void *)0))) {
		(void)fprintf(stderr, "Assertion failed, bad pointer in link: file \"%s\", line %d\n", file, line);
		abort();
	}
	return (pl.next->data);
}

/**
 * @brief
 *      get previous entry in list
 *
 * @param[in] pl - list variable
 * @param[in] file -file name
 * @param[in] line - line num
 *
 * @retuan      Void
 *
 */
void *
get_prev(pbs_list_node pl, char *file, int line)
{
	if ((pl.prev == (pbs_list_node *)0) ||
		((pl.prev == &pl) && (pl.data != (void *)0))) {
		(void)fprintf(stderr, "Assertion failed, null pointer in link: file \"%s\", line %d\n", file, line);
		abort();
	}
	return (pl.prev->data);
}
#endif

/**
 * @brief
 * 	-list_move - move an entire list from one head to another
 *
 * @param[in] from - pointer to pbs_list_node
 * @param[in] to - pointer to pbs_list_node
 *
 * @return	Void
 */

void
list_move(pbs_list_node *from, pbs_list_node *to)
{
	if (from->next == from) {
		to->next = to;
		to->prev = to;
	} else {
		to->next = from->next;
		to->next->prev = to;
		to->prev = from->prev;
		to->prev->next = to;
		CLEAR_HEAD((*from));
	}
}
